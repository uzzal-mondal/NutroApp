package com.example.nutroapp.schedule;

public class Month {

    private int id;
    private String month;

    public Month(int id, String month) {
        this.id = 0;
        this.month = "";
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }
}
